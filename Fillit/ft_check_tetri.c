#include "libft.h"

int			ft_check_tetri(long n)
{
	long	tab[10];
	int		i;

	i = 0;
	tab[0] = 110011;
	tab[1] = 1100011;
	tab[2] = 11011;
	tab[3] = 1000110001;
	tab[4] = 10011001;
	tab[5] = 111001;
	tab[6] = 100010011;
	tab[7] = 1000100011;
	tab[8] = 100110001;
	tab[9] = 110010001;
	while (i <= 9)
	{
		if (tab[i] == n)
			return (1);
		i++;
	}
	return (0);
}

