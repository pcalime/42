#include "libft.h"

int		convert_bit_to_char(char *str)
{
	int	ret;

	ret = 0;
	ret += (str[0] - 48) * 128;
	ret += (str[1] - 48) * 64;
	ret += (str[2] - 48) * 32;
	ret += (str[3] - 48) * 16;
	ret += (str[4] - 48) * 8;
	ret += (str[5] - 48) * 4;
	ret += (str[6] - 48) * 2;
	ret += (str[7] - 48);
	return (ret);
}
