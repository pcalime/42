#include "libft.h"

void	ft_putendl(char const *s)
{
	int	cmpt;

	cmpt = 0;
	while (s[cmpt])
	{
		ft_putchar(s[cmpt]);
		cmpt++;
	}
	ft_putchar('\n');
}
