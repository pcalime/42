#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int	cmpt;

	cmpt = 0;
	while (s[cmpt])
	{
		ft_putchar_fd(s[cmpt], fd);
		cmpt++;
	}
}
